// 표 셀 헬퍼
const cell = (text, header = false, shade = null) => {
  const opts = {
    children: [new Paragraph({
      children: [new TextRun({ text, bold: header, size: header ? 20 : 19, font: FONT })],
      spacing: { after: 0 },
    })],
    margins: { top: 100, bottom: 100, left: 140, right: 140 },
  }
  if (shade) opts.shading = { fill: shade, type: 'clear' }
  return new TableCell(opts)
}

const tbl = (headers, rows, colWidths = null) => {
  const totalCols = headers.length
  const defaultPct = Math.floor(100 / totalCols)
  return new Table({
    width: { size: 100, type: WidthType.PERCENTAGE },
    rows: [
      new TableRow({
        children: headers.map((h) => cell(h, true, 'dbe9ff')),
        tableHeader: true,
      }),
      ...rows.map((row, ri) => new TableRow({
        children: row.map((c) => cell(c, false, ri % 2 === 0 ? 'f7f9ff' : null)),
      })),
    ],
  })
}

